a = 5
b = 2

print("Addition:", a + b)          # 7
print("Subtraction:", a - b)       # 3
print("Multiplication:", a * b)    # 10
print("Division:", a / b)          # 2.5 (float division)
print("Exponentiation:", a ** b)   # 25 (5 to the power of 2)
print("Floor Division:", a // b)   # 2 (integer division, floor)
print("Modulo:", a % b)             # 1 (remainder)
