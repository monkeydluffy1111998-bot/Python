# --------------------------------
# 🔍 Relational / Comparison Operators
# --------------------------------

a = 50
b = 20

print("a == b:", a == b)   # Equal to → False
print("a != b:", a != b)   # Not equal to → True
print("a > b:", a > b)     # Greater than → True
print("a < b:", a < b)     # Less than → False
print("a >= b:", a >= b)   # Greater than or equal to → True
print("a <= b:", a <= b)   # Less than or equal to → False

# Additional Example:
x = 10
y = 10
print("x == y:", x == y)   # True, because both are equal
print("x != y:", x != y)   # False, because both are equal
