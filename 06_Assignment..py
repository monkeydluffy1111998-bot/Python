# Assignment Operators examples:

num = 10

num **= 5    # num = num ** 5 (Exponentiation)
print("After **= :", num)

num = 10
num += 5     # num = num + 5 (Addition)
print("After += :", num)

num = 10
num -= 5     # num = num - 5 (Subtraction)
print("After -= :", num)

num = 10
num /= 5     # num = num / 5 (Division)
print("After /= :", num)

num = 10
num %= 5     # num = num % 5 (Modulus)
print("After %= :", num)

# Simple assignment
num = 10
num = 5      # num is now 5 (overwrites previous value)
print("After simple assignment:", num)
